/**
 * Hitung Z-score WHO menggunakan metode LMS
 * @param {number} X  - nilai pengukuran (tinggi/berat)
 * @param {number} L  - nilai L (Box-Cox power)
 * @param {number} M  - nilai M (Median)
 * @param {number} S  - nilai S (Coefficient of Variation)
 * @returns {number} Z-score
 */
function whoCalcZscore(X, L, M, S) {
  let Z;
  if (Math.abs(L) < 0.0001) {
    // Jika L = 0, gunakan log-normal
    Z = Math.log(X / M) / S;
  } else {
    Z = (Math.pow(X / M, L) - 1) / (L * S);
  }

  // Koreksi WHO untuk nilai ekstrem (|Z| > 3 SD)
  if (Math.abs(Z) > 3) {
    let SD3pos, SD3neg, SD23pos, SD23neg;
    if (Math.abs(L) < 0.0001) {
      // L = 0
      SD3pos = M * Math.exp(S * 3);
      SD3neg = M * Math.exp(S * (-3));
      SD23pos = M * Math.exp(S * 3) - M * Math.exp(S * 2);
      SD23neg = M * Math.exp(S * (-2)) - M * Math.exp(S * (-3));
    } else {
      SD3pos = M * Math.pow(1 + L * S * 3, 1 / L);
      SD3neg = M * Math.pow(1 + L * S * (-3), 1 / L);
      SD23pos = SD3pos - M * Math.pow(1 + L * S * 2, 1 / L);
      SD23neg = M * Math.pow(1 + L * S * (-2), 1 / L) - SD3neg;
    }
    if (X > SD3pos) Z = 3 + (X - SD3pos) / SD23pos;
    else if (X < SD3neg) Z = -3 + (X - SD3neg) / SD23neg;
  }

  return Z;
}

/**
 * Ambil nilai LMS dari tabel berdasarkan usia (bulan)
 * @param {Array} tabel - tabel LMS (format [[bulan, L, M, S], ...])
 * @param {number} usiaBulan - usia dalam bulan
 * @returns {{L, M, S}} atau null
 */
function whoGetLMSByAge(tabel, usiaBulan) {
  const usia = Math.min(Math.max(Math.round(usiaBulan), 0), 60);
  const row = tabel.find(r => r[0] === usia);
  if (!row) return null;
  return { L: row[1], M: row[2], S: row[3] };
}

/**
 * Ambil nilai LMS dari tabel berdasarkan tinggi badan (cm), dengan interpolasi linear
 * @param {Array} tabel - tabel LMS (format [[tinggi_cm, L, M, S], ...])
 * @param {number} tinggiCm - tinggi badan dalam cm
 * @returns {{L, M, S}}
 */
function whoGetLMSByHeight(tabel, tinggiCm) {
  const tb = Math.min(Math.max(tinggiCm, tabel[0][0]), tabel[tabel.length - 1][0]);
  for (let i = 0; i < tabel.length - 1; i++) {
    if (tabel[i][0] <= tb && tabel[i + 1][0] >= tb) {
      if (tabel[i][0] === tb) return { L: tabel[i][1], M: tabel[i][2], S: tabel[i][3] };
      const t = (tb - tabel[i][0]) / (tabel[i + 1][0] - tabel[i][0]);
      return {
        L: tabel[i][1] + t * (tabel[i + 1][1] - tabel[i][1]),
        M: tabel[i][2] + t * (tabel[i + 1][2] - tabel[i][2]),
        S: tabel[i][3] + t * (tabel[i + 1][3] - tabel[i][3])
      };
    }
  }
  const last = tabel[tabel.length - 1];
  return { L: last[1], M: last[2], S: last[3] };
}

/**
 * Hitung ketiga Z-score WHO (TB/U, BB/U, BB/TB) sekaligus
 * @param {number} bb          - berat badan (kg)
 * @param {number} tb          - tinggi badan (cm)
 * @param {number} usiaBulan   - usia dalam bulan
 * @param {string} jenisKelamin - 'L' atau 'P'
 * @returns {{tbu, bbu, bbtb, status, statusString}}
 */
function whoHitungSemua(bb, tb, usiaBulan, jenisKelamin) {
  const isBoy = jenisKelamin === 'L';
  const tabelHAZ = isBoy ? WHO_HAZ_BOYS : WHO_HAZ_GIRLS;
  const tabelWAZ = isBoy ? WHO_WAZ_BOYS : WHO_WAZ_GIRLS;
  const tabelWHZ = isBoy ? WHO_WHZ_BOYS : WHO_WHZ_GIRLS;

  // Z-score TB/U (Height-for-Age)
  const lmsTBU = whoGetLMSByAge(tabelHAZ, usiaBulan);
  const tbu = lmsTBU ? parseFloat(whoCalcZscore(tb, lmsTBU.L, lmsTBU.M, lmsTBU.S).toFixed(2)) : null;

  // Z-score BB/U (Weight-for-Age)
  const lmsBBU = whoGetLMSByAge(tabelWAZ, usiaBulan);
  const bbu = lmsBBU ? parseFloat(whoCalcZscore(bb, lmsBBU.L, lmsBBU.M, lmsBBU.S).toFixed(2)) : null;

  // Z-score BB/TB (Weight-for-Height)
  const lmsBBTB = whoGetLMSByHeight(tabelWHZ, tb);
  const bbtb = lmsBBTB ? parseFloat(whoCalcZscore(bb, lmsBBTB.L, lmsBBTB.M, lmsBBTB.S).toFixed(2)) : null;

  // Klasifikasi stunting berdasarkan TB/U (standar WHO):
  // Normal       : Z-score TB/U >= -2 SD
  // Stunting     : -3 SD <= Z-score TB/U < -2 SD
  // Sangat Pendek : Z-score TB/U < -3 SD
  let status, statusString;
  if (tbu === null) {
    status = 'normal'; statusString = 'Normal';
  } else if (tbu >= -2) {
    status = 'normal'; statusString = 'Normal';
  } else if (tbu >= -3) {
    status = 'stunting'; statusString = 'Stunting';
  } else {
    status = 'severe'; statusString = 'Sangat Pendek';
  }

  return { tbu, bbu, bbtb, status, statusString };
}

/**
 * Hitung usia balita dalam bulan dari tanggal lahir
 * @param {string} tglLahir - tanggal lahir format 'YYYY-MM-DD'
 * @returns {number} usia dalam bulan
 */
function whoHitungUsiaBulan(tglLahir) {
  if (!tglLahir) return 0;
  const lahir = new Date(tglLahir);
  const now = new Date();
  let tahun = now.getFullYear() - lahir.getFullYear();
  let bulan = now.getMonth() - lahir.getMonth();
  if (bulan < 0) { tahun--; bulan += 12; }
  return tahun * 12 + bulan;
}
